fruits = ["apple", "banana", "cherry"]
#remove (by_value)
fruits.remove("banana")
print (fruits)

#pop (by index, default last)
fruits.pop(0)
print (fruits) #apple removed



name = ["shirshadip","ram","shyam","sita","gita"]
print (name)
#del (by_index)
del name [0]#remove first element
print (name) #shirshadip removed

#clear(remove all)
name.clear()
print (name) #list is now empty