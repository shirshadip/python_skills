fruits = ["apple", "banana", "cherry", "orange", "grapes", "pear", "melon", "kiwi", "mango"]
fruits_copy = fruits.copy()
print(fruits_copy)  # Output: ['apple', 'banana', 'cherry', 'orange', 'grapes', 'pear', 'melon', 'kiwi', 'mango']

#copy from index 1 to 3 
fruits_slice = fruits[1:4]
print(fruits_slice)  # Output: ['banana', 'cherry', 'orange']