"""⚙️ Step 2: Universal Functions (UFuncs)

NumPy provides many built-in mathematical functions that work fast on arrays."""

import numpy as np
arr= np.array([1,4,9,16,25])

print (np.sqrt(arr)) #square root 
print (np.exp(arr))  #exponential(e^x)
print (np.log(arr)) #natural log 
print (np.sin(arr)) #sine values
print (np.mean(arr))#average 
print (np.max(arr))#maximum
print (np.min(arr))#minimum
print (np.sum(arr))#sum